#!/usr/bin/sh

# Copy .env.example to .env if .env does not exist
if [ ! -f .env ]; then
  cp .env.example .env
  echo ".env file created from .env.example"
fi

# Laravel setup
php artisan key:generate
php artisan storage:link
php artisan migrate
php artisan db:seed

# Node setup
npm install
npm run build












































